<h3> ▷ 性格 </h3>
<p class="underline-blue">极度注重效率且完美主义的A型血性格</p>
<ul>
<li>只能跟同等级的A型血性格的人共享仓库。</li>
<li>具备多任务处理能力。一边开车一边操作手机是家常便饭。可以做到多个事业并行进行。</li>
<li>家具和贴纸的位置调整要精确到小数点以下。</li>
<li>讨厌整理垃圾物品，觉得这是浪费时间的行为，必须要把归为垃圾的物品摆得乱七八糟才会消气。</li>
<li>专心开车是少有的行为。为了追求驾驶路线的效率而冒险，所以经常发生事故。</li>
</ul>
<p class="underline-blue">冷酷且利己主义</p>
<ul>
<li>很少表露感情，基本不会流泪或者是表现出寂寞的样子。</li>
<li>认为正确与否与情感上的喜好是两回事。</li>
<li>不穿的衣服或者是不用的物品会立即处理掉，不管是不是由他人所赠与的。</li>
<li>房间非常简洁且不会布置装饰用的家具，只会放置最低限度必要的物品。</li>
<li>不习惯接受别人的恩惠，对来自他人的祝贺或者礼物会感到不自在。</li>
</ul>
<p class="underline-blue">坚持自己信念的性格</p>
<ul>
<li>看重恩情和义理，做事讲道理。</li>
<li>遇到无法接受的事情，或者是跟自己有关的人受到伤害时，会立即与造成原因的组织进行直接交涉。</li>
<li>对他人转述的意见仅作为参考，判断依据仅限于直接从本人口中得出的意见。</li>
</ul>
<p class="underline-blue">非常会照看他人，很重视与他人的交往和联系</p>
<ul>
<li>经常倾听别人的烦恼，如果对方展示诚意，他也会给予同等程度的尊重。</li>
<li>能敏锐地察觉对方痛苦的情绪，看到他人烦恼时，会主动上前打招呼。</li>
<li>强烈仰慕身为旧识的MonD，即使离开了他，仍然对“MonD是这里最强的”这件事深信不疑。</li>
</ul>
